public class mathmax {
    public static void main(String[] args) {
        int a = 52;
        int b = 65;
        double x = 3.65;
        double y = 6.25;

        System.out.println("The minimum of a and b is = " + Math.max(a, b));
        System.out.println("The minimum of x and y = " + Math.max(x, y));
    }
}
